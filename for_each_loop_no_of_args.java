public class for_each_loop_no_of_args {

    public static void main (String[] args) {
    	int n=65;
    	char ch;
    	ch=(char)n;
		System.out.println(ch);
		ch='b';
		n=ch;
		System.out.print(ch);
		System.out.print(n);
    }
    
    
}